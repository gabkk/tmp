#include <string.h>

char	*ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	i = 0;
	if (!s || !f)
		return (NULL);
	while (s[i] != '\0')
	{
		(*f)(i, (s + i));
		i++;
	}
	return (NULL);
}
