#include <string.h>

char		*ft_strchr(const char *s, int c)
{
	char	d;

	d = (char)c;
	while (*s && *s != d)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	if (*s == d)
		return ((char *)s);
	return (NULL);
}
