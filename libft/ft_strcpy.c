char	*ft_strcpy(char *dest, const char *src)
{
	int i;

	i = 0;
	if (dest)
	{
		if (!src)
			return (dest);
		while (src[i] != '\0')
		{
			dest[i] = *(char *)(src + i);
			i++;
		}
		dest[i] = '\0';
	}
	return (dest);
}
