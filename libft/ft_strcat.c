#include <stdlib.h>

char	*ft_strcat(char *s1, const char *s2)
{
	char		*str1;
	char		*str2;

	if (!s2 || !s1)
		return (s1);
	str1 = s1;
	str2 = (char *)s2;
	while (*str1 != '\0')
		str1++;
	while (*str2 != '\0')
		*str1++ = *str2++;
	*str1 = '\0';
	return (s1);
}
