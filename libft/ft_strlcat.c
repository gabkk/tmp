#include "libft.h"

size_t		ft_strlcat(char *dest, const char *src, size_t size)
{
	char		*d;
	char		*s;
	int			n;
	int			dlen;

	d = (char *)dest;
	s = (char *)src;
	n = (int)size;
	while (n-- != 0 && *d != '\0')
		d++;
	dlen = d - dest;
	n = size - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - (char *)src));
}
